class Operaciones{
  public static float sumaFloat(float a, float b){
    return(a+b);
  }

  public static float restaFloat(float a,float b){
    return(a-b);
  }

  public static float multiFloat(float a,float b){
    return(a*b);
  }

  public static float diviFloat(float a,float b){
    return(a/b);
  }

}
